use crate::source::Span;

#[derive(Debug, Clone, PartialEq)]
pub enum TokenKind {
    LeftParen,
    RightParen,
    LeftBracket,
    RightBracket,
    LeftBrace,
    RightBrace,
    Comma,
    Colon,
    Dot,
    Semicolon,
    Pipe,
    Question,
    Plus,
    Minus,
    Star,
    Slash,
    Bang,
    EqualEqual,
    BangEqual,
    Greater,
    GreaterEqual,
    Less,
    LessEqual,
    Identifier(String),
    String(String),
    Number(f64),
    Let,
    Const,
    Set,
    Be,
    Print,
    If,
    Then,
    Else,
    End,
    While,
    For,
    In,
    Function,
    Async,
    Await,
    Class,
    Inherit,
    Protocol,
    Implements,
    Field,
    Private,
    Readonly,
    Static,
    Public,
    This,
    Super,
    Is,
    Import,
    As,
    Return,
    Try,
    Catch,
    Throw,
    True,
    False,
    Nil,
    And,
    Or,
    Not,
    Eof,
}

#[derive(Debug, Clone, PartialEq)]
pub struct Token {
    pub kind: TokenKind,
    pub line: usize,
    pub column: usize,
    pub span: Span,
}

impl Token {
    pub fn new(kind: TokenKind, span: Span) -> Self {
        Self {
            kind,
            line: span.line,
            column: span.column,
            span,
        }
    }
}
